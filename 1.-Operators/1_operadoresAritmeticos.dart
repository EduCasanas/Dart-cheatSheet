void main() {
  int num1 = 20;
  int num2 = 3;

  print(num1 + num2); // SUMA resultado=23
  print(num1 - num2); // RESTA resultado=17
  print(num1 * num2); // MULTIPLICACION resultado=60
  print(num1 / num2); // DIVISION resultado=6.6666666666666667
  print(num1 ~/ num2); // DIVISION ENTERA resultado=6

  print(num1 % num2); //MODULO resultado=2 es el residuo de la division
  print(-num1); //INVERTIR el signo de un numero
}
