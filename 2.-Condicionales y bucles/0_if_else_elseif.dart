void main() {
  int calificacion = 5;

  if (calificacion == 10) {
    print('Pasaste!');
  } else if (calificacion == 9) {
    print('Muy bien!');
  } else if (calificacion == 8) {
    print('Bien!');
  } else if (calificacion == 7) {
    print('Regular!');
  } else if (calificacion == 6) {
    print('Suficiente!');
  } else {
    print('No pasaste');
  }
}
